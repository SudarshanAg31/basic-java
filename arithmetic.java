//basic arithmetic operator * + \ -
public class arithmetic {
    public static void main(String[] args){
        int x=50;
        float y=30;
        System.out.println(x+y);//use to add to number
        System.out.println(x-y);//use to sub two number
        System.out.println(x*y);//use to mult to number
        System.out.println(x/y);//use to div two number
//NOTE:in case of(/)it return int div for float div change "int y" to "float y"
}
    
}
